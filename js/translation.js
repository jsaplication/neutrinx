    const currentLang = localStorage.getItem('lang') || navigator.language.slice(0, 2);
    let lang = ['en', 'pt'].includes(currentLang) ? currentLang : 'en';

    function translatePage() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                el.textContent = translations[lang][key];
            }
        });

        // Update current language display
        document.getElementById('current-flag').src = lang === 'en' ? 'https://flagcdn.com/w20/us.png ' : 'https://flagcdn.com/w20/br.png ';
        document.getElementById('current-language').textContent = lang.toUpperCase();
    }

    document.querySelectorAll('.language-option').forEach(option => {
        option.addEventListener('click', (e) => {
            e.preventDefault();
            lang = option.getAttribute('data-lang');
            localStorage.setItem('lang', lang);
            translatePage();
        });
    });

    window.addEventListener('DOMContentLoaded', () => {
        translatePage();
    });